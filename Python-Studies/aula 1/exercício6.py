"""
Calcule a média de um aluno
O float vem no input pois precisamos tratar o dado
no momento de sua entrada
"""""

Nota_1 = float(input("Nota da primeira prova"))

Nota_2 = float(input("Nota da segunda prova"))

print(f"A média do aluno no primeiro bimestre é: {(Nota_1 + Nota_2)/2 }")

Nota_3 = float(input("Nota da terceira prova"))

Nota_4 = float(input("Nota da quarta prova"))

media = (Nota_3 + Nota_4) / 2

print(f"A média do aluno no"
      f"segundo bimestre é: {media}")